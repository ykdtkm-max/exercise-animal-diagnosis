print("# 運動タイプ診断 METs（非運動性熱産生）換算マスターデータ（実践提案カタログ）\n")
print("本ドキュメントは、発散させた約700のMETs項目から、ユーザーが日常でイメージしやすく、そのままアクションとして提案できる具体的な粒度（約130項目）を厳選・再構築したバランス型の収束版カタログです。\n")
print("「子供とサッカーする」「テーマパークで1日遊ぶ」「1駅手前で降りて歩く」など、多くのユーザーが挑戦しやすく、かつユニークでカテゴリに偏りのない網羅性を意識してリストアップしています。\n")
print("> **消費カロリー算出公式**\n")
print("> 消費カロリー（kcal） ＝ METs × 体重（60kgを想定） × 時間（1時間） × 1.05\n")
print("> ※小数点以下は四捨五入しています。（1.0 METs ≒ 63 kcal）\n\n---\n")

for cat_name, items in categories:
    print(f"## {cat_name}\n")
    print("| 運動項目（活動内容） | METs | 1時間の消費カロリー (60kg) |")
    print("| :--- | :--- | :--- |")
    for name, mets in items:
        kcal = calc_kcal(mets)
        print(f"| {name} | {mets:.1f} | **{kcal} kcal** |")
    print("\n")
