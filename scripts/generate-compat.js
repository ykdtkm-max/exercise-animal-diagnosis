function getInvitePlainForActivity(act, kind) {
  const row = INVITE_PLAIN_BY_ID[act.id];
  if (!row) return '';
  return kind === 'suggest' ? row.invite_suggest_plain : row.invite_when_invited_plain;
}

/**
 * @param {'suggest'|'invited'} invitePlainKind — tip_a_invites_b は suggest（誘う側）、tip_b_invites_a は invited（誘われた側の視点の一文）
 */
function generateTip(act, inviterCode, inviteeCode, inviterAxes, inviteeAxes, diffs, invitePlainKind) {
  const parts = diffs.map(diff => {
    const vInviter = inviterAxes[diff];
    const vInvitee = inviteeAxes[diff];
    const key = `${vInviter}_invites_${vInvitee}`;
    const fn = TIP_TEMPLATES[diff]?.[key];
    return fn ? fn(inviterCode, inviteeCode) : null;
  }).filter(Boolean);

  let core;
  if (parts.length === 0) {
    core = `2人のスタイルは似ているため、誘い方はシンプルに「一緒に行こう」で十分です。日時と場所を決めるだけでスムーズに進みます。`;
  } else {
    core = parts.slice(0, 2).join(' また、');
  }
  const invitePlain = getInvitePlainForActivity(act, invitePlainKind);
  if (invitePlain) {
    return `${core} ${invitePlain}`;
  }
  return core;
}

