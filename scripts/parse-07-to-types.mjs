#!/usr/bin/env node
/**
 * Parses 07_16タイプ診断結果コンテンツ.md into JSON for exercise-v2 prototype.
 *
 * Fields per type:
 *   tagline, concept, concept_title, catchphrases,
 *   suit_text, suit_title, unsuited_text, unsuited_title,
 *   continuation_text, continuation_title,
 *   compatible_types: { good: [{code,name,reason},...], bad: [...] }
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const mdPath = path.join(__dirname, '../要件/02_診断ロジック/07_16タイプ診断結果コンテンツ.md');
const raw = fs.readFileSync(mdPath, 'utf8');

const chunks = raw.split(/\n## \[/).slice(1);

function field(block, id) {
  const re = new RegExp(
    `\\*\\*${id}[^\\n]*:\\*\\*\\s*\\n([\\s\\S]*?)(?=\\n\\*\\*[A-E][0-9][^\\n]*:\\*\\*|\\n---|\\n## \\[|$)`
  );
  const m = block.match(re);
  return m ? m[1].trim() : '';
}

function parseCompatible(text) {
  const lines = text.split('\n').map((l) => l.trim()).filter((l) => l.startsWith('- ['));
  const good = [];
  const bad = [];
  for (const l of lines) {
    const kind = (l.match(/^- \[(good|bad)\]/) || [])[1];
    const code = (l.match(/code:\s*(\w+)/) || [])[1];
    const name = (l.match(/name:\s*([^/]+)/) || [])[1]?.trim();
    const reason = (l.match(/reason:\s*(.+)$/) || [])[1]?.trim() || '';
    if (!code || !kind) continue;
    (kind === 'good' ? good : bad).push({ code, name, reason });
  }
  return { good, bad };
}

const types = {};

for (const chunk of chunks) {
  const header = chunk.match(/^(\w+)\]\s*(.+)/);
  if (!header) continue;
  const code = header[1];
  const typeName = header[2].trim();

  const tagline = field(chunk, 'A2');
  const concept = field(chunk, 'A3');
  const concept_title = field(chunk, 'A3T');
  const catchRaw = field(chunk, 'A4');
  const catchphrases = catchRaw
    .split('\n')
    .map((l) => l.trim())
    .filter(Boolean)
    .map((l) => l.replace(/^「/, '').replace(/」$/, '').trim());

  const suit_text          = field(chunk, 'B1G');
  const suit_title         = field(chunk, 'B1GT');
  const unsuited_text      = field(chunk, 'B1B');
  const unsuited_title     = field(chunk, 'B1BT');
  const continuation_text  = field(chunk, 'C1');
  const continuation_title = field(chunk, 'C1T');
  const d3 = field(chunk, 'D3');
  const compatible_types = parseCompatible(d3);

  types[code] = {
    code,
    type_name: typeName,
    tagline,
    concept,
    concept_title,
    catchphrases,
    suit_text,
    suit_title,
    unsuited_text,
    unsuited_title,
    continuation_text,
    continuation_title,
    compatible_types,
  };
}

const dir = path.join(__dirname, '../prototype/exercise-v2');
fs.mkdirSync(dir, { recursive: true });
const json = JSON.stringify(types, null, 2);
fs.writeFileSync(path.join(dir, 'types-parsed.json'), json, 'utf8');
fs.writeFileSync(
  path.join(dir, 'types-data.js'),
  `/* Auto-generated by scripts/parse-07-to-types.mjs — do not edit by hand */\nwindow.EXERCISE_V2_TYPES = ${JSON.stringify(types)};\n`,
  'utf8'
);
console.log('Wrote types-parsed.json + types-data.js keys:', Object.keys(types).sort().join(', '));
