// ─────────────────────────────────────────────
// 軸スコア計算
// answers: 各問の回答 (1-4, nullは未回答)
// 返値: 8軸スコア [ax1..ax8], 各軸 [-1, +1]
// ─────────────────────────────────────────────
export function calculateAxisScores(
  answers: (number | null)[],
  questions: ExerciseQuestion[],
): number[] {
  const rawSums = new Array(8).fill(0);
  const counts = new Array(8).fill(0);

  questions.forEach((q, i) => {
    const a = answers[i];
    if (a === null || a === undefined) return;
    // a ∈ {1,2,3,4} → directedScore ∈ {0,1,2,3}
    const directedScore = q.direction === 1 ? (a - 1) : (4 - a);
    rawSums[q.axis - 1] += directedScore;
    counts[q.axis - 1]++;
  });

  return rawSums.map((sum, i) => {
    const count = counts[i];
    if (count === 0) return 0;
    const midPoint = (count * 3) / 2; // 5問なら7.5
    const raw = (sum - midPoint) / midPoint; // [-1, +1]
    return Math.max(-1, Math.min(1, raw * 1.3)); // 増幅係数1.3でclamp
  });
}

// ─────────────────────────────────────────────
// 欲求スコア計算
// axisScores: 8軸スコア (各 [-1, +1])
// 返値: 10欲求スコア (各 [0, 100])
// ─────────────────────────────────────────────
export function calculateDesireScores(
  axisScores: number[],
): Record<DesireKey, number> {
  const keys: DesireKey[] = ['健康', '精神', '外見', '成長', '習慣', '仲間', '交流', '娯楽', '承認', '競争'];
  const result = {} as Record<DesireKey, number>;

  keys.forEach(key => {
    const weights = desireWeights[key];
    const maxScore = desireMaxScores[key];
    let raw = 0;
    for (let i = 0; i < 8; i++) {
      raw += axisScores[i] * weights[i];
    }
    // [-maxScore, +maxScore] → [0, 100]
    result[key] = Math.min(100, Math.max(0, Math.round((raw + maxScore) / (2 * maxScore) * 100)));
  });

  return result;
}

// ─────────────────────────────────────────────
// タイプ判定（3段階）
// Step1: 軸1 → Solo / Group
// Step2: 軸8 → 屋外志向か否か
//   Solo+屋外: 軸4(強度)でS-冒険 or S-ライフ
//   Group+強屋外: 軸7(社会質)でG-フェス or G-旅行
// Step3: 欲求スコア上位でSolo6/Group6タイプを決定
// ─────────────────────────────────────────────
export function determineExerciseType(
  axisScores: number[],
  desireScores: Record<DesireKey, number>,
): TypeCode {
  const ax1 = axisScores[0]; // Solo<0, Group>0
  const ax4 = axisScores[3]; // 安定<0, 追い込み>0
  const ax7 = axisScores[6]; // 体験快楽<0, 関係絆>0
  const ax8 = axisScores[7]; // 室内<0, 屋外>0

  if (ax1 < 0) {
    // ── Solo路線 ──────────────────────────────────────────
    if (ax8 > 0.2) {
      // 屋外志向あり → 冒険 or ライフスタイル
      return ax4 > 0 ? 'S-冒険' : 'S-ライフ';
    }
    // 室内 → Solo欲求上位で決定
    const soloDesires: DesireKey[] = ['精神', '健康', '習慣', '成長', '外見', '競争'];
    const top = soloDesires.reduce((a, b) => desireScores[a] >= desireScores[b] ? a : b);
    return `S-${top}` as TypeCode;

  } else {
    // ── Group路線 ────────────────────────────────────────
    if (ax8 > 0.35) {
      // 屋外・イベント志向強 → フェス or 旅行
      // 軸7 < 0 = 体験・快楽型 → フェス参加型
      // 軸7 ≥ 0 = 関係・絆型 → 仲間との旅行・遠征
      return ax7 < 0 ? 'G-フェス' : 'G-旅行';
    }
    // 室内コミュニティ → Group欲求上位で決定
    // Solo欲求が1位の場合も Group文脈でマッピング
    const groupMap: Record<DesireKey, TypeCode> = {
      仲間: 'G-仲間',
      交流: 'G-交流',
      娯楽: 'G-娯楽',
      競争: 'G-競争',
      健康: 'G-健康',
      承認: 'G-承認',
      精神: 'G-健康', // ウェルネス仲間
      習慣: 'G-健康', // グループ健康クラブ
      成長: 'G-仲間', // CrossFit・道場コミュニティ
      外見: 'G-承認', // SNS・コンテスト系
    };
    const allDesires = Object.keys(desireScores) as DesireKey[];
    const top = allDesires.reduce((a, b) => desireScores[a] >= desireScores[b] ? a : b);
    return groupMap[top];
  }
}

// ─────────────────────────────────────────────
// ヘルパー: 欲求スコアを表示順にソートして返す
// ─────────────────────────────────────────────
export function sortedDesires(
  desireScores: Record<DesireKey, number>,
): Array<{ key: DesireKey; score: number }> {
  return (Object.keys(desireScores) as DesireKey[])
    .map(k => ({ key: k, score: desireScores[k] }))
    .sort((a, b) => b.score - a.score);
}
